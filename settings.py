from pydantic_settings import BaseSettings, SettingsConfigDict


class Settings(BaseSettings):
    model_config = SettingsConfigDict(
        env_file=".env",
        env_file_encoding="utf-8",
        extra="ignore",
    )

    # Системный промпт ассистента: задаёт стиль, язык и базовые ограничения ответа.
    SYSTEM_PROMPT: str = (
        "Ты — ИИ-ассистент Джассея. Отвечай только на русском, чётко и по делу."
        "\nНикаких других языков, кроме русского (кроме кода и терминов)."
        "\nЕсли не знаешь — скажи честно. На неэтичные запросы корректно откажи."
    )
    # Промпт архивариуса: определяет формат и правила извлечения фактов в JSON.
    SUMMARY_PROMPT: str = (
        "Ты Архивариус памяти.\n"
        "Верни ТОЛЬКО JSON вида {{\"facts\":[\"...\"]}}.\n"
        "Если фактов нет: {{\"facts\":[]}}.\n"
        "Правила:\n"
        "1) Не выдумывай. Бери только из сообщения user.\n"
        "2) Каждый факт должен быть сформулирован строго относительно пользователя (его целей, предпочтений, решений, действий, информации о пользователе и т.д.).\n"
        "3) 1 факт = 1 короткая строка.\n"
        "4) Сохраняй только полезное надолго: предпочтения пользователя, постоянные данные пользователя, цели пользователя, договоренности пользователя, ограничения для пользователя.\n"
        "5) Факты из строк A: НЕ используй, за исключением явного подтверждения фактов, уже сказанных в строках U:. Строки A: не должны становиться источником новых фактов, только подтверждать факты от U:.\n"
        "6) Удаляй дубли и шум.\n"
        "7) Пиши по-русски.\n"
        "8) Максимум {max_facts} фактов.\n"
        "9) Никакого текста вне JSON.\n"
    )
    # Промпт judge-модуля: решает, как соотнести новый факт со старым.
    FACT_JUDGE_PROMPT: str = (
        "Сравни старый и новый факт.\n"
        "Определи отношение: новый факт заменяет старый, дополняет его или это одно и то же.\n"
        "Ответь одним словом: REPLACE, ADD или IGNORE.\n"
        "Бери данные только из сообщения user."
    )

    # Размер окна сообщений, после которого запускается суммаризация памяти.
    SUMMARY_EVERY_N_MESSAGES: int = 8
    # Максимум фактов, которые архивариус может вернуть за один batch.
    SUMMARY_MAX_FACTS: int = 7
    # Лимит простого поиска фактов (legacy-параметр, если где-то используется напрямую).
    MEMORY_SEARCH_LIMIT: int = 10
    # Нижний порог похожести: ниже этого факты считаются недостаточно близкими.
    FACT_SIMILARITY_LOW: float = 0.8
    # Верхний порог похожести: выше этого факты считаются почти идентичными.
    FACT_SIMILARITY_HIGH: float = 0.95

    # Модель judge-агента для принятия решений по конфликтующим фактам.
    FACT_JUDGE_MODEL: str = "qwen2.5:1.5b-instruct"
    # Базовая модель ассистента для генерации ответа пользователю.
    BASE_MODEL: str = "qwen-8k:latest"
    # Температура базовой модели: ниже значение — более стабильные ответы.
    BASE_MODEL_TEMPERATURE: float = 0.1
    # top_k базовой модели: ограничивает набор токенов-кандидатов на шаге генерации.
    BASE_MODEL_TOP_K: int = 40
    # top_p базовой модели: ограничивает "ядро" вероятностного распределения токенов.
    BASE_MODEL_TOP_P: float = 0.85

    # URL API LLM-провайдера (локальный или удалённый endpoint совместимого API).
    LLM_BASE_URL: str = "http://localhost:11434/v1"
    # API-ключ LLM-провайдера.
    LLM_API_KEY: str = "ollama"
    # Хост Qdrant, где хранится долговременная память.
    QDRANT_HOST: str = "localhost"
    # Порт Qdrant.
    QDRANT_PORT: int = 6333

    # Таймаут judge-вызова в секундах, чтобы не блокировать pipeline слишком долго.
    FACT_JUDGE_TIMEOUT_SEC: int = 20
    # Флаг подробных логов memory pipeline для диагностики и отладки.
    MEMORY_PIPELINE_LOGS_ENABLED: bool = True
    # Температура архивариуса при суммаризации диалога в факты.
    ARCHIVIST_TEMPERATURE: float = 0.1
    # Температура judge-модуля (обычно 0 для детерминированных решений).
    FACT_JUDGE_TEMPERATURE: float = 0.0

    # Минимальный score векторного поиска, чтобы факт считался релевантным.
    MEMORY_MIN_VECTOR_SCORE: float = 0.72
    # Модель rerank-агента для дополнительной переоценки релевантности фактов.
    MEMORY_RERANK_MODEL: str = "qwen2.5:1.5b-instruct"
    # Температура rerank-модуля.
    MEMORY_RERANK_TEMPERATURE: float = 0.0
    # Вес rerank-оценки в финальном скоринге фактов.
    MEMORY_RERANK_WEIGHT: float = 0.6
    # Вес исходного векторного score в финальном скоринге фактов.
    MEMORY_VECTOR_WEIGHT: float = 0.4

    # Сколько кандидатов максимально брать из векторного поиска перед rerank.
    MEMORY_RETRIEVAL_TOP_K: int = 20
    # Сколько фактов оставлять после rerank-этапа.
    MEMORY_RERANK_TOP_N: int = 6
    # Сколько фактов максимум добавлять в системный контекст ответа.
    MEMORY_CONTEXT_FACTS_LIMIT: int = 4
    # Включатель rerank-этапа: при False используется только векторный score.
    MEMORY_RERANK_ENABLED: bool = True


settings = Settings()
